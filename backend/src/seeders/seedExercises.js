const seedExercises = async () => {
  try {
    await sequelize.sync();
    
    // Проверяем, есть ли уже упражнения
    const existingExercises = await Exercise.count();
    if (existingExercises === 0) {
      await Exercise.bulkCreate(initialExercises);
      console.log('✅ Начальные упражнения добавлены в базу данных');
    } else {
      console.log('✅ В базе данных уже есть упражнения');
    }
  } catch (error) {
    console.error('❌ Ошибка при добавлении начальных упражнений:', error);
  }
};

